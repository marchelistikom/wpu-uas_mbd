            <h2>Tentang Tentara Nasional Indonesia</h2>
            <p>
                Tentara Nasional Indonesia (TNI) adalah komponen utama kekuatan pertahanan negara Indonesia. Dibentuk dari perjuangan rakyat untuk mempertahankan kemerdekaan, TNI merupakan garda terdepan dan benteng terakhir penjaga kedaulatan Negara Kesatuan Republik Indonesia (NKRI). Sebagai alat negara di bidang pertahanan, TNI memiliki tanggung jawab besar dalam menjaga stabilitas keamanan nasional dari berbagai ancaman, baik dari dalam maupun luar negeri. Keberadaan TNI tidak hanya sebatas kekuatan militer, namun juga menjadi bagian integral dari kehidupan berbangsa dan bernegara, dengan nilai-nilai Pancasila dan UUD 1945 sebagai landasan utamanya.
            </p>
            <h3>Tugas Pokok TNI</h3>
            <p>
                Tugas pokok TNI, sesuai Undang-Undang Nomor 34 Tahun 2004 tentang Tentara Nasional Indonesia, adalah menegakkan kedaulatan negara, mempertahankan keutuhan wilayah NKRI yang berdasarkan Pancasila dan UUD 1945, serta melindungi segenap bangsa dan seluruh tumpah darah Indonesia dari ancaman dan gangguan terhadap keutuhan bangsa dan negara. Tugas ini dilaksanakan melalui Operasi Militer untuk Perang (OMP) dan Operasi Militer Selain Perang (OMSP). OMP mencakup tugas seperti penindakan agresi, penanggulangan terorisme, dan pengamanan perbatasan. Sementara OMSP meliputi tugas bantuan kemanusiaan, penanggulangan bencana alam, pengamanan objek vital nasional, dan mendukung operasi perdamaian dunia.
            </p>
            <h3>Visi dan Misi TNI 2025</h3>
            <p>
                **Visi TNI 2025:** Menjadi kekuatan pertahanan negara yang profesional, modern, tangguh, dan disegani di tingkat regional maupun global, serta senantiasa siap sedia melindungi kedaulatan dan kepentingan nasional, menjamin keutuhan wilayah, dan keselamatan bangsa Indonesia.
            </p>
            <p>
                **Misi TNI 2025:**
                <ol>
                    <li>Membangun postur pertahanan yang kuat melalui modernisasi Alutsista dan pengembangan SDM prajurit yang berkualitas.</li>
                    <li>Meningkatkan profesionalisme prajurit TNI dalam setiap operasi dan tugas, didukung oleh doktrin dan latihan yang relevan.</li>
                    <li>Mengembangkan kapabilitas intelijen dan siber untuk menghadapi ancaman hibrida dan non-konvensional.</li>
                    <li>Memperkuat sinergi antara TNI, Polri, Kementerian/Lembaga, dan seluruh komponen bangsa dalam sistem pertahanan semesta.</li>
                    <li>Berpartisipasi aktif dalam upaya perdamaian dunia dan kerjasama militer internasional untuk mempromosikan stabilitas regional.</li>
                    <li>Meningkatkan kesejahteraan prajurit dan keluarganya sebagai bentuk penghargaan atas dedikasi dan pengorbanan mereka.</li>
                </ol>
            </p>
